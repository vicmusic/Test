package com.victor.examen;

public class CalculadoraJava {

    
    public Integer sumar(Integer valor1, Integer valor2) {
        Integer resultado;

        resultado = valor1 + valor2;

        return resultado;

    }

    public Integer restar(Integer valor1, Integer valor2) {
        Integer resultado;
        resultado = valor1 - valor2;
        return resultado;
    }

    public Integer multiplicar(Integer valor1, Integer valor2) {
        Integer resultado;
        resultado = valor1 * valor2;
        return resultado;
    }

    public Integer dividir(Integer valor1, Integer valor2) {
        Integer resultado;
        resultado = valor1 / valor2;
        return resultado;
    }



    public CalculadoraJava() {
        
    }
    

}
